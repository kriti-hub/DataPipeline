function getStatusColor(value, config) {
  if (!config.good) return "text-brand-600 bg-brand-50";
  if (config.invertThreshold) {
    if (value <= config.good) return "text-green-700 bg-green-50";
    if (value <= config.warn) return "text-amber-700 bg-amber-50";
    return "text-red-700 bg-red-50";
  }
  if (value >= config.good) return "text-green-700 bg-green-50";
  if (value >= config.warn) return "text-amber-700 bg-amber-50";
  return "text-red-700 bg-red-50";
}

export default function KPICard({ metricKey, value }) {
  const config = KPI_CONFIG[metricKey];
  if (!config) return null;
  const statusColor = getStatusColor(value, config);
  const formattedValue = formatKPI(value, config.format);

  return (
    <div className="card p-5 flex flex-col">
      <div className="flex items-center justify-between mb-3">
        <span className="text-sm font-medium text-gray-500">{config.label}</span>
        <div className={`p-2 rounded-lg ${statusColor}`}>
          {ICONS[config.icon]}
        </div>
      </div>
      <div className="text-2xl sm:text-3xl font-bold text-gray-900 tracking-tight">
        {formattedValue}
      </div>
    </div>
  );
}
